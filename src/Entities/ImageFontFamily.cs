namespace ImgFlip4NET
{
    /// <summary>
    ///     A set of meme font families supported by ImgFlip.
    /// </summary>
    public enum ImageFontFamily
    {
        /// <summary>
        ///     The impact default font.
        /// </summary>
        Impact,

        /// <summary>
        ///     Arial font family.
        /// </summary>
        Arial,

        /// <summary>
        ///     The default font family used for memes by ImgFlip.
        /// </summary>
        Default = Impact
    }
}